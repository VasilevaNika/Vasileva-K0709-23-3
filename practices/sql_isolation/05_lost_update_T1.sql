-- ============================================================
-- АНОМАЛИЯ: LOST UPDATE (Потерянное обновление)
-- ТРАНЗАКЦИЯ T1 — выполняется в Терминале 1
-- ============================================================
--
-- ПОРЯДОК ВЫПОЛНЕНИЯ:
--   [T1-1] Выполните блок "[T1-1]" -> T1 читает баланс Bob (500)
--   [T2-1] Переключитесь на Терминал 2, выполните [T2-1]
--   [T1-2] Вернитесь, выполните [T1-2] -> T1 обновляет и коммитит
--   [FIN]  Проверьте итоговый баланс — он должен быть НЕВЕРНЫМ
--
-- ТЕОРИЯ: Lost update — обе транзакции читают одну строку,
-- затем обе её обновляют. Результат второго UPDATE перезаписывает
-- первый, и одно из обновлений "теряется".
--
-- СЦЕНАРИЙ: Bob хочет пополнить счёт на 200 (T1).
-- Одновременно кассир зачисляет кэшбэк 100 (T2).
-- Итог должен быть: 500 + 200 + 100 = 800.
-- Из-за Lost Update получится только 700.
--
-- Уровень изоляции, при котором воспроизводится: READ COMMITTED
-- Предотвращается: SELECT FOR UPDATE, атомарным UPDATE,
--                  уровнем REPEATABLE READ (PostgreSQL)
-- ============================================================

-- ===================== [T1-1] =====================

BEGIN;
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

SELECT id, name, balance FROM accounts WHERE name = 'Bob';

-- ===================== [T1-2] =====================
-- Выполните ПОСЛЕ того как T2 закоммитила свой UPDATE
-- T1 не знает, что T2 уже изменила баланс, и записывает 700

UPDATE accounts SET balance = 700.00 WHERE name = 'Bob';

COMMIT;

SELECT id, name, balance FROM accounts WHERE name = 'Bob';
