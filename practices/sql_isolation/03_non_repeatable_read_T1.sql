-- ============================================================
-- АНОМАЛИЯ: NON-REPEATABLE READ (Неповторяющееся чтение)
-- ТРАНЗАКЦИЯ T1 — выполняется в Терминале 1
-- ============================================================
--
-- ПОРЯДОК ВЫПОЛНЕНИЯ:
--   [T1-1] Выполните блок "[T1-1]" -> T1 читает баланс (1000.00)
--   [T2-1] Переключитесь на Терминал 2, выполните [T2-1]
--   [T1-2] Вернитесь, выполните [T1-2] -> T1 читает баланс СНОВА
--
-- ТЕОРИЯ: Non-repeatable read — в рамках одной транзакции
-- T1 дважды читает одну строку и получает РАЗНЫЕ значения,
-- потому что между чтениями другая транзакция T2 изменила
-- и закоммитила эту строку.
--
-- Уровень изоляции, при котором воспроизводится: READ COMMITTED
-- Предотвращается на уровне: REPEATABLE READ и выше
-- ============================================================

-- ===================== [T1-1] =====================

BEGIN;
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

SELECT id, name, balance FROM accounts WHERE name = 'Alice';

-- ===================== [T1-2] =====================
-- Выполните ПОСЛЕ того как T2 закоммитила изменение

SELECT id, name, balance FROM accounts WHERE name = 'Alice';

COMMIT;
