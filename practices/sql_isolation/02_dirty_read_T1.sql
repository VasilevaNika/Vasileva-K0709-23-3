-- ============================================================
-- АНОМАЛИЯ: DIRTY READ (Грязное чтение)
-- ТРАНЗАКЦИЯ T1 — выполняется в Терминале 1
-- ============================================================
--
-- ПОРЯДОК ВЫПОЛНЕНИЯ (чередуйте терминалы):
--
--   [T1-1] Выполните блок "[T1-1]" -> T1 обновляет баланс (без COMMIT)
--   [T2-1] Переключитесь на Терминал 2, выполните [T2-1] и [T2-2]
--   [T1-2] Вернитесь в Терминал 1, выполните блок "[T1-2]"
--   [T2-3] Переключитесь на Терминал 2, выполните [T2-3]
--
-- ТЕОРИЯ: Dirty Read — T2 читает данные, изменённые T1,
-- которая ещё НЕ сделала COMMIT. Если T1 откатится —
-- T2 работала с несуществующими данными.
-- PostgreSQL предотвращает эту аномалию даже на уровне
-- READ UNCOMMITTED (трактует его как READ COMMITTED).
-- ============================================================

-- ===================== [T1-1] =====================

BEGIN;
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;

SELECT id, name, balance FROM accounts WHERE name = 'Alice';

-- Меняем баланс, но НЕ коммитим
UPDATE accounts SET balance = 9999.00 WHERE name = 'Alice';

-- ===================== [T1-2] =====================
-- Выполните ПОСЛЕ шагов T2-1 и T2-2

ROLLBACK;

SELECT id, name, balance FROM accounts WHERE name = 'Alice';
