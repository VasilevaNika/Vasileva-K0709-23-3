--   Переключитесь на Терминал 1, выполните [T1-2]
-- ============================================================

-- ===================== [T2-1] =====================
-- Выполняется ПОСЛЕ того как T1 прочитала баланс, но ДО записи T1

BEGIN;
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

SELECT id, name, balance FROM accounts WHERE name = 'Bob';

-- T2 зачисляет кэшбэк: 500 + 100 = 600
UPDATE accounts SET balance = 600.00 WHERE name = 'Bob';

COMMIT;

SELECT id, name, balance FROM accounts WHERE name = 'Bob';
